	@Override
	@Transactional
	public void createCourseAndStudent(int courseId, List<Student> students) {
		Optional<Course> byId = courseRepository.findById(courseId);
		
		if (byId.isPresent()) {
			Course course = byId.get();
			
			students.stream().forEach(e -> {
				course.addStudent(e);
			});
			
			courseRepository.save(course);
		}
	}

